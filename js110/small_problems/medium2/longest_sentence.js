const SENTENCE_ENDERS = ['.', '!', '?'];

function getSentences(str) {
  let result = [];
  let wordsArr = str.split(" ");
  let sentence = "";

  wordsArr.forEach(word => {
    let lastChar = word[word.length - 1];

    if (!SENTENCE_ENDERS.includes(lastChar)) {
      sentence += (word + " ");
    } else {
      sentence += word;
      result.push(sentence);
      sentence = "";
    }
  });

  return result;
}

function longestSentence(str) {
  let sentences = getSentences(str);
  let longestSentence = sentences[0];
  let longestWordCount = longestSentence.split(" ").length;

  for (let idx = 0; idx < sentences.length; idx += 1) {
    let currentSentence = sentences[idx];
    let currentWordCount = currentSentence.split(" ").length;

    if (currentWordCount > longestWordCount) {
      longestSentence = currentSentence;
      longestWordCount = currentWordCount;
    }
  }

  console.log(`${longestSentence}\n`);
  console.log(`The longest sentence is ${longestWordCount} words`);
}

